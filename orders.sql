-- For each store, find the staff member who has processed the highest number of orders.
with NumORders as
(select store_name, first_name, last_name, count(orders.staff_id) as TotalOrders,
rank() over(partition by store_name order by count(orders.staff_id) desc) as ranking from staffs
left join orders on orders.staff_id = staffs.staff_id
left join stores on stores.store_id = staffs.staff_id
group by store_name, first_name, last_name)

select store_name, first_name, TotalOrders from NumORders
where ranking = 1

-- Calculate the total discount amount given for each order, but only for orders that were shipped later than their required_date.
select * from orders, order_items
select orders.order_id, required_date, shipped_date, sum(quantity*list_price*discount) as Discount from orders
left join order_items on orders.order_id = order_items.order_id
where shipped_date > required_date
group by orders.order_id, required_date, shipped_date


-- Write a query to generate a report showing the category_name, total quantity sold, and total sales amount (after applying the discount) for the year 2017, grouped by category and sorted by total sales in descending order.
select 
category_name, 
sum(quantity) as TotalQuantity,
sum((order_items.list_price*quantity) - (discount*quantity)) as TotalDiscountedSales
from order_items
join products on products.product_id =order_items.product_id
join categories on categories.category_id = products.category_id
join orders on orders.order_id = order_items.order_id
where shipped_date between '2017-01-01' and '2017-12-31'
group by category_name
order by TotalDiscountedSales desc


